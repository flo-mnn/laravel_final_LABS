     */
    public function update(Request $request, Footer $footer)
    {
        $validate = $request->validate([
            'copyright'=>'required|max:255',
            'designed_by'=>'max:255',
            'designer'=>'max:255',
        ]);
        $footer->copyright = $request->copyright;
